//! bleep-interop/src/layer3_bridge.rs
//! BLEEP Connect Layer 3: Post-Quantum ZK Proof Bridge
//!
//! Layer 3 provides cryptographically-sound cross-chain value transfers using
//! post-quantum transparent proofs derived from transcript commitments and
//! SPHINCS+ signatures. Unlike Layer 4 (economic slashing), Layer 3 security
//! is based on PQ signatures and deterministic proof binding.
//!
//! # Proof integrity
//! L3BatchProver::prove_batch calls bleep_connect_layer3_zkproof::ProofGenerator::generate_proof
//! and verifies the resulting post-quantum proof using a deterministic PQ signing key.

use std::collections::HashMap;
use std::fmt;
use std::time::Instant;

use bleep_connect_layer3_zkproof::{ProofGenerator, ProofInput, ProofVerifier};
use bleep_connect_types::{BleepConnectResult, ProofType};

